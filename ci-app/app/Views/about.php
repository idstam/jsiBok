
<h1>Information om tjänsten</h1>
<p>
Det här är en tjänst som bokför för ditt företags transaktioner från Skatteverkets skattekonto i Fortnox eller Visma.
</p>
<p>Servicebyråer kan dessutom få sina klienters skattekontotransaktioner med epost oavsett koppling till Fortnox eller Visma.</p>

<p>Tjänsten drivs av JSI Skattekonto AB (559323-5533).</p>
<p> Kontakt: <a href="mailto:info@huvudboken.se">info@huvudboken.se</a></p>

<h2>Cookies</h2>
<p>Tjänsten använder cookies för att hantera sina administrativa funktioner.</p>
<p>Tjänsten använder inga cookies från tredje part.</p>
<p>Tjänsten lämnar inte ut några användaruppgifter till tredje part, förutom vid eventuella förfrågningar från myndigheter.</p>

<h2>Datalagring</h2>
<p>Tjänsten sparar den information som efterfrågas för att hantera kopplingen mellan bokföringen och Skatteverket.</p>
<p>Varken bokföringssystemet eller Skatteverket har kunskap om varandra med anledning av tjänsten.</p>
<p>Tjänsten kan lagra skattekontots transaktioner och saldo samt huvudbokens saldon på inblandade balanskonton för att sköta felhantering och avstämning.</p>
<p>Lagrade epostadresser används enbart för kommunikation som rör tjänstens funktionalitet.</p>
<p>Tjänstens driftsmiljö och/eller infrastruktur kan lagra ip-adresser. I den mån tjänsten har kontroll över uppgifterna kan de lämnas ut till efterfrågande myndigheter.</p>
<p>Auditloggning sker hos Skatteverket av inkomna
förfrågningar via API:et och dessa bevaras i upp till 5år. Auditloggar används av Skatteverket för att förebygga och utreda säkerhetsincidenter.</p>

<h2>Funktionalitet</h2>
<p>Initialt skapas "bara" bokföring för transaktionerna på skattekontot. Tjänsten gör ingen avstämning av konton.</p>
<p>Ambitionen är att kontrollera skattekontot varje dag, men förändringar hos endera bokföringssystemet eller Skatteverket samt oplanerade sericeavbrott kan försena bokföringen.</p>


<h2>Kända brister</h2>
<p>Det finns inte dokumentation från Skatteverket som täcker alla möjliga transaktioner som kan dyka upp.</p>
<p>När en okänd transatktionstext dyker upp reder vi ut bokföringen och gör den åt er inom ett par dagar.</p>
<p>Transaktioner som flyttar saldo till/från kronofogden använder 2510 som motkonto.</p>
<p>'Rättelse enligt beslut' använder 2510 som motkonto, eftersom vi inte vet vad beslutet avser.</p>
<p>'Tillfälligt betalningsanstånd upphör' använder 2510 som motkonto, eftersom vi inte vet vad anståndet avsåg.</p>
<p>'Jordbruksåterbetalning' utan information om energislag bokas mot 5310.</p>

<h2>Ansvar för bokföringen</h2>
<p>JSI Skattekonto AB tar inte över något ansvar för redovisningen, se till att göra en kontroll åtminstone i samband med bokslut.</p>

<h2>Betalning</h2>
<p>Betalning sker per påbörjad kalendermånad. Antingen via din faktura från Fortnox, eller via din servicebyrå.</p>

<h2>Uppsägning och avslut</h2>
<p>Du kan när som helst avsluta integrationen genom att ta bort applikationen i Fortnox eller Visma. Glöm inte att <a  target="_blank" href="https://www.skatteverket.se/foretag/etjansterochblanketter/allaetjanster/tjanster/ombudochbehorigheter.4.18e1b10334ebe8bc80005609.html">avanmäla oss som ombud</a> för ditt skattekonto. </p>
<p>JSI Skattekonto AB kan avsluta integrationen med tre månaders varsel, med undantag för oförutsedda händelser som förhindrar fortsatt drift av tjänsten.
